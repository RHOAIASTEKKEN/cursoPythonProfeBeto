num1 = 100
num2 = 50

num1 +=1
num2 -=1

# operadores aritmeticos

suma = num1 + num2
resta = num1 - num2
multiplicacion = num1 * num2
division = num1 / num2
porcentaje = num1 % num2
incremento = num1
decremento = num2

# operadores falto o verdadero relacionales
mayorQue = num1 > num2
menorQue = num1 < num2
mayorOIgualQue = num1 >= num2
menorOIgualQue = num1 <= num2
igualQue = num1 == num2

# operadores logicos condicionales
andY = (num1 > num2) and (num1 < num2)
orO = (num1 > num2) or (num1 < num2)
notNegation= not(num1 > num2)


print("\nsuma --> ", suma,
      "\nresta --> ", resta,
      "\nmultiplicacion --> ", multiplicacion,
      "\ndivision --> ", division,
      "\nporcentaje --> ", porcentaje,
      "\nincremento --> ", incremento,
      "\ndecremento --> ", decremento,
      "\nmayor que --> ", mayorQue,
      "\nmenor que --> ", menorQue,
      "\nmayor o igual que --> ", mayorOIgualQue,
      "\nmenor o igual que --> ", menorOIgualQue,
      "\nigual que --> ", igualQue,
      "\nand --> ", andY,
      "\nor --> ", orO,
      "\nnot --> ", notNegation,)